    public function search($query, $page = 1, $resultsPerPage = 5) {
        $queryWords = explode(" ", $query);
        $results = [];

        foreach ($queryWords as $queryWord) {
            if (array_key_exists($queryWord, $this->index)) {
                $results = array_merge($results, $this->index[$queryWord]);
            } else {
                // Implement improved spelling correction and suggestion
                $suggestions = $this->suggestSpellingCorrection($queryWord);
                if ($suggestions) {
                    foreach ($suggestions as $suggestion) {
                        if (array_key_exists($suggestion, $this->index)) {
                            $results = array_merge($results, $this->index[$suggestion]);
                        }
                    }
                }
            }
        }

        $sortedResults = $this->rankResults($results, $query);

        // Implement pagination
        $startIdx = ($page - 1) * $resultsPerPage;
        $endIdx = $startIdx + $resultsPerPage;
        $paginatedResults = array_slice($sortedResults, $startIdx, $endIdx);

        return $paginatedResults;
    }

   
    public function rankResults($results, $query) {
        $relevanceScores = [];
        foreach ($results as $document) {
            $relevanceScores[$document] = $this->calculateRelevance($document, $query);
        }

        arsort($relevanceScores);

        return array_keys($relevanceScores);
    }

    public function calculateRelevance($document, $query) {
        $queryWords = explode(" ", $query);
        $allWords = $document->title . " " . $document->content;
        $matchingTerms = 0;

        foreach ($queryWords as $word) {
            if (strpos($allWords, $word) !== false) {
                $matchingTerms++;
            }
        }

        return $matchingTerms / max(count($queryWords), 1);
    }
